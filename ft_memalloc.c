#include "ft_printf.h"

void	*ft_memalloc(size_t size)
{
	char	*mem;
	size_t	i;

	mem = (void *)malloc(size);
	if (!mem)
		return (NULL);
	i = 0;
	while (i < size)
	{
		mem[i] = 0;
		i++;
	}
	return (mem);
}
